func getPokemon(url string, cache *pokecache.Cache) (PokemonJSON,error){
	pokemonJSON := PokemonJSON{}
	data, exists := cache.Get(url)
	if exists {
		json.Unmarshal(data, &pokemonJSON)
		return pokemonJSON,nil
	}
	body,err := getURL(url) 
	if err!=nil{
		return pokemonJSON,err
	}
	json.Unmarshal(body, &pokemonJSON)
	cache.Add(url, body)

	return pokemonJSON, nil
}

func getURL(url string) ([]byte,error){
	body:= []byte{}	
	res,err := http.Get(url)
	if err!=nil{
		log.Fatal(err)
		return body,err
	}
	body,err = io.ReadAll(res.Body)
	res.Body.Close()
	if res.StatusCode > 299 {
		fmt.Printf("Response failed with status code %d and body: %s\n", res.StatusCode, string(body))
		return body, err
	}
	if err != nil {
		log.Fatal(err)
		return body, err
	}
	return body,nil
}