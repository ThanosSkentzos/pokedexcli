func getMap(url string, cache *pokecache.Cache) (MapJSON, error) {

	// fmt.Printf("Requesting map data...")
	mapJSON := MapJSON{}
	data, exists := cache.Get(url)
	if exists {
		json.Unmarshal(data, &mapJSON)
		return mapJSON, nil
	}

	body,err := getURL(url) 
	if err!=nil{
		return mapJSON,err
	}
	json.Unmarshal(body, &mapJSON)
	cache.Add(url, body)
	// fmt.Println("Done.")
	return mapJSON, nil
}

